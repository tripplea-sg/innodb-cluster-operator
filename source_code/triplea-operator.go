func ClusterLogic(podName string, namespace string) error {
	var err error

	if strings.Trim(thisPod.member_state, "\t \n") == "RECOVERING" || strings.Trim(thisPod.member_state, "\t \n") == "ONLINE" {
		// we don't want operator doing anything when the state is ONLINE or RECOVERING
	} else {

	if strings.Trim(thisPod.gtid_mode, "\t \n") != "ON" {
		sts_addinstance = true
		err = ConfigureInstance(podName, namespace)
		thisPod, err = setIndentity(podName, namespace)
		sts_addinstance = false
	}

	if strings.Trim(thisPod.gtid_mode, "\t \n") == "ON" && len(strings.Trim(thisPod.member_role, "\t \n")) == 0 && len(strings.Trim(thisPod.member_state, "\t \n")) == 0 && thisPod.PodIndex == 0 && len(strings.Trim(thisPod.gtid_executed, "\t \n")) == 0 {
		sts_addinstance = true
		err = CreateCluster(podName, namespace)
		thisPod, err = setIndentity(podName, namespace)
		sts_addinstance = false
	}

	if strings.Trim(thisPod.gtid_mode, "\t \n") == "ON" && len(strings.Trim(thisPod.member_role, "\t \n")) == 0 {
		sts_addinstance = true
		err = RegisterInstance(podName, namespace)
		thisPod, err = setIndentity(podName, namespace)
		sts_addinstance = false
	}

	if strings.Trim(thisPod.gtid_mode, "\t \n") == "ON" && len(strings.Trim(thisPod.member_role, "\t \n")) == 0 && (strings.Trim(thisPod.member_state, "\t \n") == "OFFLINE" || len(strings.Trim(thisPod.member_state, "\t \n")) == 0)  && len(strings.Trim(thisPod.gtid_executed, "\t \n")) != 0 && len(strings.Trim(thisPod.serverid, "\t \n")) != 0 {
		sts_addinstance = true
		RebootClusterFromCompleteOutage()
		sts_addinstance = false
	}

		// this space is for future development purposes	
		// any new logic add here		



	}	

	// if this node is PRIMARY then has to do regular rescan to clean up members that leaving cluster
 	if strings.Trim(thisPod.member_role, "\t \n") == "PRIMARY" {
		err = refreshInstance(podName)
	}	


	return err 
}

func Local_CP(podname string, NameSpace string) error {
	var ForeverLoop = true
	var err error
	
	// loop forever
        for ForeverLoop {
		glog.Info(">> NEW LCP CYCLE")
                err = try.Do(func(attempt int) (retry bool, err error) {
                        thisPod, err = setIndentity(podname, NameSpace)
                       	err = ClusterLogic(podname, NameSpace)
 			return attempt < 5, err // try 5 times
                })
                if err != nil {
                        // glog.Error(err)
			glog.Info(`CLP-cycle: System maybe busy and wasn't responsive, will try again`)
                }
		glog.Info(">> END OF LCP CYCLE")
		time.Sleep(8 * time.Second)
        }
	return err
}

