func repl(PodName string, master_user string, master_password string) {
	var cmd, StatefulsetName, NameSpace, Hostname, check_exist, primary_node, member_role string
	var clustersize int
	var err error

	// get statefulset
	StatefulsetName, _ = GetParentNameAndOrdinal(PodName)

	// get namespace
	err = try.Do(func(attempt int) (retry bool, err error) {
		Hostname, err = os.Hostname() 
		NameSpace, err = getNamespace(PodName)
		return attempt < 5, err // try 5 times
        })
	
	if err != nil {

	}

	for {

	// Get target cluster size
	clustersize = 0
	for {
		err = try.Do(func(attempt int) (retry bool, err error) {
			PodName = StatefulsetName + `-` + strconv.Itoa(clustersize)
			cmd = `kubectl get pod | grep ` + PodName + ` | wc -l`
			check_exist, err = OScmd(cmd)
			glog.Info("We found this pod " + strings.Trim(check_exist, "\t \n") + " " + PodName)
			return attempt < 5, err // try 5 times
		})
		if strings.Trim(check_exist, "\t \n") != `0` {
			clustersize += 1
			glog.Info("Number of cluster node is increased to " + strconv.Itoa(clustersize))
		} else {
			glog.Info("So, the total cluster member for now is " + strconv.Itoa(clustersize))
			break
		}
	}	

	if clustersize > 0 {

	glog.Info("Find the current primary node")

	// get Primary
	for i := 0; i < clustersize; i++ {
		PodName = StatefulsetName + `-` + strconv.Itoa(i)
		glog.Info("Check if " + PodName + " is primary node")
		err = try.Do(func(attempt int) (retry bool, err error) {
			member_role, _, err =  ExecToPodThroughAPI(`mysql -uroot -p` + mysql_root_password + ` -s -N -e "select member_role from performance_schema.replication_group_members where member_host='` +  PodName + `'"`, "mysql", PodName, NameSpace, nil)
			return attempt < 5, err // try 5 times
                })
		if strings.Trim(member_role, "\t \n") == "PRIMARY" {
			primary_node = PodName
			break
		}
	}
	glog.Info("Primary Node is: " + primary_node)
	
	glog.Info("Check if replication channel is exist")
 	// check if replication channel is exist
	err = try.Do(func(attempt int) (retry bool, err error) {
		check_exist, _, err = ExecToPodThroughAPI(`mysql -uroot -p` + mysql_root_password + ` -s -N -e "select count(1) from performance_schema.replication_connection_configuration where channel_name='autochannel'"`, "mysql", primary_node, NameSpace, nil)
		return attempt < 5, err // try 5 times
        })
	glog.Info("Variable check_exist is " + check_exist)

	glog.Info("Perform action for replication")
	if strings.Trim(check_exist, "\t \n") == `0` {
		// create and start replication channel
		glog.Info("Create replication channel on " + primary_node)
		err = try.Do(func(attempt int) (retry bool, err error) {
			cmd = `mysql -uroot -p` + mysql_root_password + ` -e "change master to master_user='` + master_user + `', master_password='` + master_password + `', master_host='` + Hostname + `', master_port=6446, master_auto_position=1 for channel 'autochannel'; change replication filter replicate_ignore_db=(mysql_innodb_cluster_metadata) for channel 'autochannel'; start slave for channel 'autochannel';"`
			_, _, err = ExecToPodThroughAPI(cmd, "mysql", primary_node, NameSpace, nil) 
			return attempt < 5, err // try 5 times
        	})
	} else {
		err = try.Do(func(attempt int) (retry bool, err error) {
			glog.Info("Start replication channel on " + primary_node)
			_, _, err = ExecToPodThroughAPI(`mysql -uroot -p` + mysql_root_password + ` -e "start slave for channel 'autochannel'"`, "mysql", primary_node, NameSpace, nil)
			return attempt < 5, err // try 5 times
                })
	}
	
	} else {
		glog.Info("Cluster not found")
	}	
	
	time.Sleep(30 * time.Second)	
	
	}	
}

