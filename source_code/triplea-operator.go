func autobackup() {
	var autoBackup bool
	var err error

	// we only want to run what does matter --> instance is ready

	for {
		autoBackup = false
		for {
			if strings.Trim(thisPod.member_state, "\t \n") == "ONLINE" {
				autoBackup = true
			}
			// we want the system to be not so busy checking
			time.Sleep(6 * time.Second)

			if autoBackup {
				break
			}
		}
		
		// ok, instance is ready --> do backup

		err = try.Do(func(attempt int) (retry bool, err error) {
			_, _, err =  ExecToPodThroughAPI(`mkdir -p /backup/rescue`, "mysql", thisPod.Hostname, thisPod.Namespace, nil)
			_, _, err =  ExecToPodThroughAPI(`rm -Rf /backup/rescue/*`, "mysql", thisPod.Hostname, thisPod.Namespace, nil)
                	_, _, err = ExecToPodThroughAPI(`mysqlsh root:` + mysql_root_password + `@localhost:3306 -e "util.dumpInstance('/backup/full')"`, "mysql", thisPod.Hostname, thisPod.Namespace, nil)
                	_, _, err =  ExecToPodThroughAPI(`cp /var/lib/mysql/auto.cnf /var/lib/mysql/mysqld-auto.cnf /backup/rescue/`, "mysql", thisPod.Hostname, thisPod.Namespace, nil)
			return attempt < 2, err // try 5 times
        	})
        	if err != nil {
			// don't wait if unsuccessful
                	// glog.Error(err)
			glog.Info(`autobackup: System maybe busy and wasn't responsive, will try again`)
        	} else {
			// wait next 12 hours if successful
			time.Sleep(12 * 3600 * time.Second)
		}		
		
	}

}

